package utils;

public class SpellLauncher {
	private int a;
	private int e;
	private int r;
	
	public SpellLauncher() {
		a = 0;
		e = 0;
		r = 0;
	}
	
	public int getA() {
		return a;
	}
	public void setA(int a) {
		this.a = a;
	}
	public int getE() {
		return e;
	}
	public void setE(int e) {
		this.e = e;
	}
	public int getR() {
		return r;
	}
	public void setR(int r) {
		this.r = r;
	}
	
}
